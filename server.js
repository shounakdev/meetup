// server.js - DEBUG VERSION
const express = require('express');
const http = require('http');
const { Server } = require('socket.io');
const cors = require('cors');

const app = express();
app.use(cors());

const server = http.createServer(app);
const io = new Server(server, {
  cors: { origin: '*', methods: ['GET','POST'] },
  maxHttpBufferSize: 10 * 1024 * 1024,
});

// In-memory room storage: roomId -> { participants: Set, host: socketId, createdAt: timestamp }
const rooms = new Map();

// Helper to cleanup empty rooms
function cleanupRoom(roomId) {
  const room = rooms.get(roomId);
  if (room && room.participants.size === 0) {
    rooms.delete(roomId);
    console.log(`üßπ Cleaned up empty room: ${roomId}`);
  }
}

// Helper to get or create room
function getOrCreateRoom(roomId) {
  if (!rooms.has(roomId)) {
    console.log(`üÜï Creating new room: ${roomId}`);
    rooms.set(roomId, {
      participants: new Set(),
      host: null,
      createdAt: Date.now()
    });
  }
  return rooms.get(roomId);
}

// Debug function to print all rooms
function printAllRooms() {
  console.log(`\nüìä === CURRENT ROOMS (${rooms.size} total) ===`);
  if (rooms.size === 0) {
    console.log(`üìä No rooms exist`);
  } else {
    for (const [roomId, room] of rooms.entries()) {
      console.log(`üìä Room ${roomId}:`);
      console.log(`   - Participants: ${room.participants.size}/2`);
      console.log(`   - IDs: [${Array.from(room.participants).join(', ')}]`);
      console.log(`   - Host: ${room.host}`);
      console.log(`   - Created: ${new Date(room.createdAt).toISOString()}`);
    }
  }
  console.log(`üìä ========================\n`);
}

io.on('connection', socket => {
  console.log(`\n‚úÖ NEW CONNECTION: ${socket.id}`);
  printAllRooms();
  
  // Store current room for this socket
  let currentRoom = null;

  socket.on('join-room', roomId => {
    console.log(`\nüö™üö™üö™ JOIN ROOM REQUEST üö™üö™üö™`);
    console.log(`Socket: ${socket.id}`);
    console.log(`Room: ${roomId}`);
    console.log(`Current room for this socket: ${currentRoom}`);
    
    const room = getOrCreateRoom(roomId);
    
    console.log(`üîç BEFORE JOIN - Room ${roomId} state:`);
    console.log(`   - Size: ${room.participants.size}`);
    console.log(`   - Participants: [${Array.from(room.participants).join(', ')}]`);
    console.log(`   - Host: ${room.host}`);
    
    // Check if user is already in the room (reconnection case)
    if (room.participants.has(socket.id)) {
      console.log(`üîÑ RECONNECTION: ${socket.id} is already in room ${roomId}`);
      currentRoom = roomId;
      socket.join(roomId);
      return;
    }

    // CRITICAL CHECK: Room size limit
    console.log(`\nüö® CHECKING ROOM LIMIT üö®`);
    console.log(`Current participants: ${room.participants.size}`);
    console.log(`Max allowed: 2`);
    console.log(`Check: ${room.participants.size} >= 2 = ${room.participants.size >= 2}`);
    
    if (room.participants.size >= 2) {
      console.log(`\n‚ùå‚ùå‚ùå ROOM FULL - REJECTING ‚ùå‚ùå‚ùå`);
      console.log(`Room: ${roomId}`);
      console.log(`Current size: ${room.participants.size}/2`);
      console.log(`Existing participants: [${Array.from(room.participants).join(', ')}]`);
      console.log(`Rejecting: ${socket.id}`);
      console.log(`Sending 'room-full' event to ${socket.id}`);
      
      socket.emit('room-full');
      
      console.log(`‚ùå room-full event sent to ${socket.id}`);
      printAllRooms();
      return;
    }

    // Add user to room
    console.log(`\n‚úÖ ADDING USER TO ROOM`);
    console.log(`Adding ${socket.id} to room ${roomId}`);
    
    room.participants.add(socket.id);
    currentRoom = roomId;
    socket.join(roomId);

    console.log(`‚úÖ User added successfully`);
    console.log(`New size: ${room.participants.size}`);
    console.log(`New participants: [${Array.from(room.participants).join(', ')}]`);

    // Set first participant as host
    if (!room.host) {
      room.host = socket.id;
      console.log(`üëë ${socket.id} is now HOST of room ${roomId}`);
    }

    console.log(`\nüéâ JOIN SUCCESS:`);
    console.log(`   - Socket: ${socket.id}`);
    console.log(`   - Room: ${roomId}`);
    console.log(`   - Participants: ${room.participants.size}/2`);
    console.log(`   - IDs: [${Array.from(room.participants).join(', ')}]`);

    // If this is the second person, notify both participants
    if (room.participants.size === 2) {
      console.log(`\nüéâüéâüéâ ROOM NOW FULL - STARTING PEER CONNECTION üéâüéâüéâ`);
      console.log(`Sending 'peer-joined' to all in room ${roomId}`);
      io.to(roomId).emit('peer-joined');
    }

    // Send room status to all participants
    const statusData = {
      participantCount: room.participants.size,
      maxParticipants: 2,
      isFull: room.participants.size >= 2
    };
    console.log(`üìä Sending room-status:`, statusData);
    io.to(roomId).emit('room-status', statusData);
    
    printAllRooms();
  });

  socket.on('check-host', (roomId, callback) => {
    console.log(`\nüëë HOST CHECK: ${socket.id} for room ${roomId}`);
    
    if (!rooms.has(roomId)) {
      console.log(`‚ùå Room ${roomId} does not exist for host check`);
      if (callback) callback(false);
      return;
    }

    const room = rooms.get(roomId);
    const isHost = room.host === socket.id;
    console.log(`üëë Host status: ${isHost} (host is: ${room.host})`);
    
    if (callback) callback(isHost);
  });

  // WebRTC Signaling Events - Fixed format to match frontend expectations
  socket.on('offer', ({ offer, roomId }) => {
    console.log(`üì§ OFFER: ${socket.id} -> room ${roomId}`, offer ? 'valid' : 'NULL/INVALID');
    if (offer && offer.type && offer.sdp) {
      socket.to(roomId).emit('offer', offer);  // Send just the offer, not wrapped
    } else {
      console.log(`‚ùå Invalid offer received:`, offer);
    }
  });

  socket.on('answer', ({ answer, roomId }) => {
    console.log(`üì§ ANSWER: ${socket.id} -> room ${roomId}`, answer ? 'valid' : 'NULL/INVALID');
    if (answer && answer.type && answer.sdp) {
      socket.to(roomId).emit('answer', answer);  // Send just the answer, not wrapped
    } else {
      console.log(`‚ùå Invalid answer received:`, answer);
    }
  });

  socket.on('ice-candidate', ({ candidate, roomId }) => {
    console.log(`üì§ ICE: ${socket.id} -> room ${roomId}`, candidate ? 'valid' : 'NULL/INVALID');
    if (candidate) {
      socket.to(roomId).emit('ice-candidate', candidate);  // Send just the candidate, not wrapped
    } else {
      console.log(`‚ùå Invalid ICE candidate received:`, candidate);
    }
  });

  // Name exchange
  socket.on('set-name', ({ roomId, name }) => {
    console.log(`üë§ NAME: ${socket.id} set name "${name}" in room ${roomId}`);
    //socket.to(roomId).emit('peer-name', { name, senderId: socket.id });
    socket.to(roomId).emit('peer-name', { name, senderId: socket.id });
  });

  // Chat messages
  socket.on('chat-message', ({ roomId, sender, message, type, timestamp }) => {
    console.log(`üí¨ CHAT: ${socket.id} in room ${roomId}`);
    socket.to(roomId).emit('chat-message', {
      sender: sender || socket.id,
      message,
      type,
      timestamp: timestamp || Date.now(),
    });
  });


  // ‚îÄ‚îÄ Screen-share signaling ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
socket.on('screen-share-started', (roomId) => {
  console.log(`üîä Screen share started by ${socket.id} in room ${roomId}`);
  socket.to(roomId).emit('screen-share-started');
});
socket.on('screen-share-stopped', (roomId) => {
  console.log(`üîä Screen share stopped by ${socket.id} in room ${roomId}`);
  socket.to(roomId).emit('screen-share-stopped');
});


  // Handle meeting end
  socket.on('end-meeting', ({ roomId, endedBy }) => {
    console.log(`\nüîö MEETING ENDED by ${endedBy} in room ${roomId}`);
    socket.to(roomId).emit('meeting-ended', { endedBy });
    
    if (rooms.has(roomId)) {
      rooms.delete(roomId);
      console.log(`üóëÔ∏è Room ${roomId} deleted after meeting end`);
    }
    printAllRooms();
  });

  // Manual leave
  socket.on('leave-room', roomId => {
    console.log(`\nüö™ MANUAL LEAVE: ${socket.id} from room ${roomId}`);
    
    if (!rooms.has(roomId)) {
      console.log(`‚ùå Room ${roomId} doesn't exist`);
      return;
    }
    
    const room = rooms.get(roomId);
    console.log(`Before leave - participants: [${Array.from(room.participants).join(', ')}]`);
    
    room.participants.delete(socket.id);
    socket.leave(roomId);
    
    console.log(`After leave - participants: [${Array.from(room.participants).join(', ')}]`);
    
    if (room.host === socket.id) {
      console.log(`üëë Host left room ${roomId}`);
      socket.to(roomId).emit('host-left');
      
      const remainingParticipants = Array.from(room.participants);
      if (remainingParticipants.length > 0) {
        room.host = remainingParticipants[0];
        console.log(`üëë New host: ${room.host}`);
      }
    } else {
      console.log(`üë§ Peer left room ${roomId}`);
      socket.to(roomId).emit('peer-left');
    }

    cleanupRoom(roomId);
    currentRoom = null;

    if (rooms.has(roomId)) {
      const updatedRoom = rooms.get(roomId);
      const statusData = {
        participantCount: updatedRoom.participants.size,
        maxParticipants: 2,
        isFull: updatedRoom.participants.size >= 2
      };
      console.log(`üìä Updated room status:`, statusData);
      io.to(roomId).emit('room-status', statusData);
    }
    
    printAllRooms();
  });

  // Disconnect cleanup
  socket.on('disconnect', reason => {
    console.log(`\n‚ùå DISCONNECT: ${socket.id} (${reason})`);
    
    if (currentRoom && rooms.has(currentRoom)) {
      const room = rooms.get(currentRoom);
      
      console.log(`Before disconnect cleanup - Room ${currentRoom}:`);
      console.log(`   - Size: ${room.participants.size}`);
      console.log(`   - Participants: [${Array.from(room.participants).join(', ')}]`);
      
      room.participants.delete(socket.id);
      
      console.log(`After disconnect cleanup - Room ${currentRoom}:`);
      console.log(`   - Size: ${room.participants.size}`);
      console.log(`   - Participants: [${Array.from(room.participants).join(', ')}]`);

      if (room.host === socket.id && room.participants.size > 0) {
        console.log(`üëë Host disconnected, notifying room`);
        socket.to(currentRoom).emit('host-left');
        
        const remainingParticipants = Array.from(room.participants);
        if (remainingParticipants.length > 0) {
          room.host = remainingParticipants[0];
          console.log(`üëë New host: ${room.host}`);
        }
      } else {
        console.log(`üë§ Peer disconnected, notifying room`);
        socket.to(currentRoom).emit('peer-left');
      }

      cleanupRoom(currentRoom);

      if (rooms.has(currentRoom)) {
        const updatedRoom = rooms.get(currentRoom);
        const statusData = {
          participantCount: updatedRoom.participants.size,
          maxParticipants: 2,
          isFull: updatedRoom.participants.size >= 2
        };
        console.log(`üìä Post-disconnect room status:`, statusData);
        io.to(currentRoom).emit('room-status', statusData);
      }
    }
    
    printAllRooms();
  });
});

// Enhanced health check
app.get('/health', (_, res) => {
  const roomDetails = {};
  for (const [roomId, room] of rooms.entries()) {
    roomDetails[roomId] = {
      participants: room.participants.size,
      participantIds: Array.from(room.participants),
      host: room.host,
      createdAt: new Date(room.createdAt).toISOString()
    };
  }
  
  res.json({ 
    status: 'OK', 
    totalRooms: rooms.size,
    rooms: roomDetails
  });
});

// Debug endpoint
app.get('/rooms/:roomId', (req, res) => {
  const roomId = req.params.roomId;
  const room = rooms.get(roomId);
  
  if (!room) {
    return res.status(404).json({ error: 'Room not found' });
  }
  
  res.json({
    roomId,
    participantCount: room.participants.size,
    participants: Array.from(room.participants),
    host: room.host,
    createdAt: new Date(room.createdAt).toISOString(),
    isFull: room.participants.size >= 2
  });
});

const PORT = process.env.PORT || 4000;
server.listen(PORT, () => {
  console.log(`üöÄ DEBUG SERVER listening on http://localhost:${PORT}`);
  console.log(`üìä Health check: http://localhost:${PORT}/health`);
  console.log(`üîç This is a DEBUG version with detailed logging`);
});

